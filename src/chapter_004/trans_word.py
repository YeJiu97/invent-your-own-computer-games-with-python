# 转义字符
# \n 换行
print("hello\nworld")
# \t 制表符
print("hello\tworld")
# \\ 反斜杠
print("hello\\world")
# \' 单引号
print("hello\'world")
# \" 双引号
print("hello\"world")
# \b 退格
print("hello\bworld")
# \r 回车
print("hello\rworld")
# \f 换页
print("hello\fworld")
# \ooo 八进制数代表的字符
print("\101\102\103")
# \xhh 十六进制数代表的字符
print("\x41\x42\x43")